package com.aes.collegemanagement.model;

public class Course {

	private int cid;
	private String name;
	
	public void setCid(int cid) {
		this.cid=cid;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	
	public int getCid() {
		return cid;
	}
	public String getName() {
		return name;
	}
}
